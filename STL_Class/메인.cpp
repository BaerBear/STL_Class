#include <iostream>
#include <array>
#include "save.h"

//-------------------------------------------------------------------------------------
// [문제] 사용자가 입력한 수만큼 int를 저장할 메모리를 확보하라.
// 1부터 시작하는 정수로 메모리를 채워라.
// 합계를 화면에 출력하라.
//-------------------------------------------------------------------------------------

//--------
int main()
//--------
{
	size_t size;
	int sum = 0;
	std::cout << "저장할 정수의 개수 : ";
	std::cin >> size;

	int* arr = new int[size];
	for (int i = 0; i < size; ++i) {
		arr[i] = i + 1;
		sum += arr[i];
	}
	std::cout << "합계 : " << sum << std::endl;
	
	delete[] arr;
	
	save("메인.cpp");
}
